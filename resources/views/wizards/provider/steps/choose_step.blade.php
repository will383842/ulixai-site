<script>
(function() {
  'use strict';
  
  function waitForWizard() {
    return new Promise((resolve) => {
      const checkInterval = setInterval(() => {
        if (typeof window.updateNavigationButtons === 'function') {
          clearInterval(checkInterval);
          resolve();
        }
      }, 50);
      
      setTimeout(() => {
        clearInterval(checkInterval);
        resolve();
      }, 5000);
    });
  }
  
  async function init() {
    await waitForWizard();
    
    const originalUpdateNav = window.updateNavigationButtons;
    
    window.updateNavigationButtons = function() {
      const step1 = document.getElementById('step1');
      
      if (step1 && !step1.classList.contains('hidden')) {
        const navButtons = document.querySelectorAll('#mobileNavButtons, #desktopNavButtons');
        navButtons.forEach(btn => btn.style.display = 'none');
        return;
      }
      
      originalUpdateNav();
    };
    
    // Navigation directe pour le bouton provider
    const providerBtn = document.getElementById('whiteCardBtn');
    if (providerBtn) {
      providerBtn.addEventListener('click', function(e) {
        e.preventDefault();
        e.stopPropagation();
        
        if (typeof window.showStep === 'function') {
          window.showStep(1);
        }
      });
    }
    
    // Gestion du popup Ulix'Map
    const ulixMapBtn = document.getElementById('ulixMapBtn');
    const ulixMapPopup = document.getElementById('ulixMapPopup');
    const closeMapPopup = document.getElementById('closeMapPopup');
    const closeMapPopupCTA = document.getElementById('closeMapPopupCTA');
    
    let autoCloseTimer = null;
    
    function openMapPopup() {
      if (ulixMapPopup) {
        ulixMapPopup.classList.remove('hidden');
        document.body.style.overflow = 'hidden';
        
        // Fermeture automatique apr√®s 6 secondes
        autoCloseTimer = setTimeout(function() {
          closeMapPopupFunc();
        }, 6000);
      }
    }
    
    function closeMapPopupFunc() {
      if (ulixMapPopup) {
        ulixMapPopup.classList.add('hidden');
        document.body.style.overflow = '';
        
        // Annuler le timer si le popup est ferm√© manuellement
        if (autoCloseTimer) {
          clearTimeout(autoCloseTimer);
          autoCloseTimer = null;
        }
      }
    }
    
    if (ulixMapBtn) {
      ulixMapBtn.addEventListener('click', function(e) {
        e.preventDefault();
        e.stopPropagation();
        openMapPopup();
      });
    }
    
    if (closeMapPopup) {
      closeMapPopup.addEventListener('click', closeMapPopupFunc);
    }
    
    if (closeMapPopupCTA) {
      closeMapPopupCTA.addEventListener('click', closeMapPopupFunc);
    }
    
    // Fermer le popup en cliquant sur le backdrop
    if (ulixMapPopup) {
      ulixMapPopup.addEventListener('click', function(e) {
        if (e.target === ulixMapPopup) {
          closeMapPopupFunc();
        }
      });
    }
    
    // Fermer avec la touche Escape
    document.addEventListener('keydown', function(e) {
      if (e.key === 'Escape' && ulixMapPopup && !ulixMapPopup.classList.contains('hidden')) {
        closeMapPopupFunc();
      }
    });
  }
  
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
})();
